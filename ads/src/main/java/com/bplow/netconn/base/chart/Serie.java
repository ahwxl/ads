package com.bplow.netconn.base.chart;

public class Serie {
	
	private String name;
	private String [] data;
	
	public Serie() {
		super();
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String[] getData() {
		return data;
	}
	public void setData(String[] data) {
		this.data = data;
	}

	
	
}
